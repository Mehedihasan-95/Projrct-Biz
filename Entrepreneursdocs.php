            <hr class="g-my-20">
           <h4 id="SalesTaxQuestions" class="pt-4">Sales Tax Questions</h4>
            <div class="d-inline-block g-width-55 g-height-2 g-bg-cyan mb-2"></div> 
            <div>
              
              <p class="pt-3"><strong>Q.</strong>
                How does the sales tax work?
              </p>
              <div>
                <p class="pt-1">
                  Sales tax is a tax paid by the <strong> end user (consumer)</strong> of a tangible product (and in some cases service) sold by a retailer. This tax is paid on a state level (there is currently no national sales tax or VAT).
                </p>
                <p class="pt-1">
                  For example, if you own electronics store in NYC, and a customer comes in and buys an item in your store, you would apply 8.875% (as of 2013) tax on top of the price paid by the customer. Then you are responsible to file a sales tax report to NY state and remit (pay) all the tax money collected from the customers.
                </p>
              </div>
           
             <p class="pt-3"><strong>Q.</strong>
              How do I know if I need to apply sales tax on the stuff I sell?
              </p>
              <div>
                <p class="pt-1">
                  Excellent question. Before reaching a conclusion you must answer three questions first:
                </p><p class="pt-1">
                  Are you selling to end users, or are you a wholesaler? Only retailers selling to end users are required to collect and remit sales tax.
                </p><p class="pt-1">
                  Does your business have nexus in any state that has sales tax? Nexus is physical connection, and we discuss it later in this article. Some states (Alaska, Delaware, Montana, New Hampshire and Oregon) have no sales tax to begin with.
                </p><p class="pt-1">
                  Is your product/service taxable to begin with? Keep in mind, most tangible goods are taxable, while most services are not, but each jurisdiction has its own rules, so it’s not that simple.
                </p>
              </div>
           
             <p class="pt-3"><strong>Q.</strong>
              Should I register in a state that has no sales tax, to avoid having to deal with it?
              </p>
              <p class="pt-1">
                Sorry, but it’s not that easy.
              </p><p class="pt-1">
                For example, let’s assume you register in Delaware (that has no sales tax) and you are selling some tangible items by shipping them from China to buyers in the U.S. Since in this case your business only has nexus in Delaware (as state of registration), you will not have to worry about sales tax at all. However, if you are using a U.S. dropshipper that ships the product from warehouses in California, Kentucky and New Jersey, technically you are required to collect sales tax from buyers of your product in all three mentioned states. 
              </p><p class="pt-1">
                If you register your company in Wyoming instead of Delaware, you add Wyoming as another state to collect sales tax in. Sounds inconvenient, but only if you assume that a serious number of consumers of your product are roaming the vast stretches of the least populated U.S. state. We are deeply in love with Wyoming, but your chances of selling anything in that state are mostly close to zero, so you might not even need to worry about registering for sales tax there until you make a few Wyoming sales first.
              </p><p class="pt-1">
                How do you register for sales tax in all these states? Read further.
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              If I am registered in one state, but my vendors are drop shipping the stuff I sell in other states - which state do I need to collect sales tax in?
              </p>
              <p class="pt-1">
                As you learned from the previous question, your company nexus spreads to all states where your dropshipper has nexus. So unless your dropshipper processes the payment side of your sales, or unless you make no sales in any of these respective states, you need to obtain permits in each of these states (as well as in your state of registration, if it has sales tax).


              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              I know that I need to collect sales tax. How do I register?
              </p>
              <p class="pt-1">
                We can help you with the registration in any state. Depending on the state this permit will be called "sales tax ID", "sales permit", "reseller permit", "vendor ID", or just "tax ID". We have this item both on its own and as part of our LLC /Corporation registration applications.
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              After I register, how do I do reporting and remit sales tax?
              </p>
              <p class="pt-1">
                You will find a good CPA (accountant) who will handle your sales tax filing as well as your income tax filing. You can try doing it on your own, but we don’t recommend it.
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              My business is registered in one state, but my dropshipper is in another. They want me to show sales tax registration in their state - how do I do it?
              </p>
              <p class="pt-1">
                Almost all states have procedures to obtain sales tax permit without having to register the company as "foreign entity". It is usually called "out-of-state vendor ID" or something of the kind. We help with these permits as well.
              </p><p class="pt-1">
                Keep in mind though, if your dropshipper is also the one processing the payment then they will be collecting sales tax and reporting it to the state themselves, so you don’t really need to obtain your own permit in case like this.
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              I want to buy products in the U.S. and sell them in my country - do I need to register for sales tax?
              </p>
              <p class="pt-1">
                Obviously you don’t need to collect sales tax in the U.S. on these sales, but you might need to collect some sort of VAT tax in the country were you sell, so check the rules there.
              </p><p class="pt-1">
                However, the real question is this - can you buy from U.S. vendors without having to pay sales tax on these purchases (in wholesale)? The answer is yes, provided you obtain sales tax ID. That’s why it’s also called "reseller permit" - you want to resell the products you buy at wholesale, to the end users, without being considered end user yourself. Check with a CPA if the state in which you obtain the permit requires you to file zero tax reports.


              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              How do I report sales tax in all the states I am registered in?
              </p>
              <p class="pt-1">
                Just as you do in one state - have your CPA file reports in each state you are registered in for sales tax, and cut checks for each state (or pay online, whatever the procedure is).
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              Do I need to register as foreign entity in states where I am registered to collect sales tax?
              </p>
              <p class="pt-1">
                Not necessarily. We distinguish the two cases as "soft nexus" and "hard nexus" (this is not official designation, we just like to call it this way).


              </p><p class="pt-1">
                "Soft nexus" has to do with connection strong enough to require you to register for sales tax (for example, if your have a dropshipper who ships from a specific state), which is usually done with state’s taxation department (or it’s equivalent), but not strong enough for registration with Secretary of State (or whatever authority registering companies in the given state)
              </p><p class="pt-1">
                "Hard nexus" is when you have physical connection to a state, for example if you have an office, warehouse, employees, or if you are managing the business from this state and are physically located there.
              </p><p class="pt-1">
                So register for sales tax only in states where you have “soft nexus”, and register foreign entity and sales tax in states where you have "hard nexus"
              </p>
           
            </div>

            <hr class="g-my-20">
            <p class="text-center">Ok, the question of sales tax should be more or less clear by now. Let’s proceed to more complex topic of income tax.</p>
            <h4 id="#Business100OwnedbyNonUSPerson" class="pt-4">U.S. Business 100% Owned by Non-U.S. Person(s)</h4>
            <div class="d-inline-block g-width-55 g-height-2 g-bg-cyan mb-2"></div> 
            <div>
              
             <p class="pt-3"><strong>Q.</strong>
              I am a single owner of a U.S. LLC, non-U.S. person living abroad. My company provides remote services. Do I need to file tax return and pay income tax?
              </p>
              <p class="pt-1">
                A single member LLC that elected to be a disregarded entity (a default election) would only pay tax based on the tax status of the owner. Since the owner is not physically present in the US and is providing services remotely there would be no income effectively connected to the US. That means the LLC would owe no US tax, except for the annual registration fee in the state of LLC registration, and there would be no US federal tax obligation (in other words there is no requirement to file income tax either).
              </p> <p class="pt-1">
                Keep in mind though - you might not technically be producing income in the U.S., but you still could be (and chances are) liable to income tax on this income in your country.


              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              What if I import and sell goods in the U.S. - does it change the previous answer?
              </p>
              <p class="pt-1">
                If your business is selling tangible goods in the US, you are required to report the income from this business to the IRS. Non-US residents report their US sourced income on form 1040NR.
              </p><p class="pt-1">
                Don’t try to figure this form out - it is our recommendation to hire a CPA to handle all your U.S. tax issues. You will also need to obtain ITIN, something your CPA will be in the best position to assist you with.
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              What if the LLC has more than one owner? What happens then?
              </p>
              <p class="pt-1">
                LLC that has more than one owner (partnership), or if it is elected to be taxed as S or C Corporation (any number of owners), must file federal tax return, even if it has zero income.
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              Ok, I got the point about LLC. But what if it’s corporation instead?
              </p>
              <p class="pt-1">
                A corporation is a separate tax entity from its owners. That means the corporation files its own tax return and pays its own tax liability. That also means that one cannot freely transfer money between the owners (shareholders) and the corporation. The corporation can reimburse the owners for expenses they pay on behalf of the business, and the corporation can pay owners for services they provide to the corporation, both of which are tax deductions for the business.
              </p>
              <p class="pt-1">
                The only other option for the shareholders to take funds from the business is if the corporation pays them dividends. Dividends are not a tax deduction and are generally taxable income to shareholders as the individuals. As a shareholder, your personal income is subject to the income tax rules in your country of residence.
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              What is the best way to reduce the taxable income of my LLC or Corporation?
              </p>
              <p class="pt-1">
                Most businesses have both revenues and expenses. The IRS keeps a list of eligible business expenses, and it is safe to say that expenses that can are obviously related to maintaining and running the business (e.g. hosting, advertising, salaries of employees, etc.) are considered deductible expenses. Other expenses might be partially deductible, and it is best to have your CPA handle the question which of your expenses are deductible and to what degree.
              </p><p class="pt-1">
                To minimize your tax obligation you would want to report as many eligible expenses as possible, however you should be able to prove these expenses were real, so keeping receipts and/or bank and credit card statements is a must.
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              Ok, let’s talk about wages. Can I pay myself a salary as a corporate officer, this way avoiding double taxation?
              </p>
              <p class="pt-1">
                If you are non-resident alien you probably don’t have work permit, which means you cannot receive a salary as a resident alien or U.S. citizen would. Sorry.
              </p> <p class="pt-1">
                You could however provide services, such as management services, to the U.S. company, and receive payment in form of consulting fees. You will then be required to report this income in accordance with your country tax rules.
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              What if we spend all or most of the income of the U.S. company on services provided by our other company, registered in our country?
              </p>
              <p class="pt-1">
                You could do that, provided you can prove services were indeed provided and properly documented. You also want to make sure these services are provided outside of the U.S., in order not to be considered U.S. sourced, and as such subject to 30% withholding requirement (more about it below).
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              What if we retain all the corporate profits in the U.S., pay the corporate income tax, and not distribute it to shareholders? Can we just reinvest this money into the business?
              </p>
              <p class="pt-1">
                Yes, you can.
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              So given all the owners are non-U.S. persons, from income tax point of view is it more beneficial to register LLC or Corporation?
              </p>
              <p class="pt-1">
                Tricky question that depends on lots of factors. Both entities have their pros and cons, so before reaching a conclusion you should analyze your specific situation, make some forecasts on how your business will evolve, and also - consult a CPA, it will help you a lot.
              </p><p class="pt-1">
                Keep in mind, there is not always a “right” and “wrong” answer - often times either entity that you would form for your business would work just fine.
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              How and when do I file tax return?
              </p>
              <p class="pt-1">
                By hiring a knowledgeable U.S. CPA (accountant). The deadline in most cases is or around April 15 (each year can be a bit different). You can file extension by that date, and the new due date is September 15 for companies and October 15 for individuals.
              </p><p class="pt-1">
                Keep in mind, corporations have to file quarterly reports, while LLCs taxed as partnerships file once a year. This could result in slightly higher cost of accounting services for corporations.
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              Do I need an ITIN to file taxes? If yes, how can I get it?
              </p>
              <p class="pt-1">
                Whether you need to obtain an ITIN will depend on if you have US tax reporting obligations due to your US business interests. It is possible that you will need an ITIN if you have membership interest (ownership) in an LLC, but most probably you won’t need one as a shareholder of a corporation.
              </p><p class="pt-1">
                KEEP IN MIND: Individuals must have a filing requirement and file a valid federal income tax return to receive an <strong class=" g-color-cyan">ITIN</strong>, unless they meet an exception.
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              What about state income tax?
              </p>
              <p class="pt-1">
                This tax is only applicable to C Corporations, not LLCs. It applies to income earned by the corporation in the state, unlike federal income that applies to all U.S. sourced income.
              </p><p class="pt-1">
                Even though LLCs don’t pay income tax, it is a good idea to check with your CPA if there are any filing requirements for the LLC in the state of registration.
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              Should I register my company in the state that has no income tax?
              </p>
              <p class="pt-1">
                Again, it doesn’t matter if you choose LLC. For corporations it matters, but only to the extent that you believe you will have lot’s of income in the state of registration. For example, if you have a Delaware Corporation and your business has no income coming from sources in Delaware then you will have no corporate tax to pay to the state of Delaware, only the federal corporate tax.
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              I heard as non-resident alien I need to pay 30% income tax on my U.S. income. Is it true?
              </p>
              <p class="pt-1">
                It is true in certain cases. It is called NRA (non-resident alien) withholding, meaning your payee keeps 30% of the sum they are paying you, and remits this sum to the IRS.
              </p><p class="pt-1">
                According to IRS rules “in order for a payment to be subject to NRA withholding, it must be a payment of FDAP income. FDAP is an acronym for Fixed or Determinable, Annual or Periodic. Some of the more common expenses paid by US withholding agents which would result in FDAP income to their vendors and other service providers are interest, royalties, compensation for personal services, rents, pensions or annuities and gains from the sale or exchange of the patents, copyrights and similar intangibles...”
              </p><p class="pt-1">
                Here is a key - for FDAP income paid to a foreign person to be subject to NRA reporting and withholding, the payment must be U.S. sourced. So how do you know if your FDAP income is in fact U.S. sourced? Here are some examples:
              </p>
              <ul class="g-list-style-disc lis-style-doc-page ml-4 pt-2">                   
                <li> <strong> Interest: </strong>If the debtor is a U.S. resident, the interest is generally U.S. sourced.</li>
                <li> <strong> Royalties:</strong> If the subject property is used in the U.S., the royalty payment is U.S. sourced. Payments made in connection with the sale of certain intangible assets, including copyrights and patents, are generally sourced similar to royalties when the payments are contingent on the productivity, use or disposition of the intangible.</li>
                <li> <strong> Rents:</strong> If the rental property is located in the U.S., the rental payment is U.S. sourced.</li>
                <li> <strong> Personal Services:</strong> If the services are performed in the U.S., the payment for those services is generally U.S. sourced.</li>
              </ul>
           
             <p class="pt-3"><strong>Q.</strong>
              What is form W-8BEN, and when do I need to file it?
              </p>
              <p class="pt-1">
                Form W-8BEN is a Certificate of Foreign Status of Beneficial Owner for United States Tax Withholding. You need to fill this form out and give to the withholding agent or payer if you are a foreign person and you are the beneficial owner of an amount subject to withholding. In other words, if you have U.S. sourced FDAP income your payer will be responsible to withhold the 30% tax based on the information listed on the W-8BEN.
              </p><p class="pt-1">
                Keep in mind, you need to submit Form W-8BEN when requested by the withholding agent or payer whether or not you are claiming a reduced rate of, or exemption from, withholding.
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              What about tax treaty between U.S. and my country? How does it influence my income tax obligations?
              </p>
              <p class="pt-1">
                If you as foreign vendor are a resident in a country that has a tax treaty with the United States, the 30% rate may be reduced. Each treaty has specific provisions which determine the reduced withholding rate. These provisions reduce the withholding rate based on the type of income and the status of the recipient.
              </p><p class="pt-1">
                To know if your country has tax treaty with the U.S. please <a class=" g-color-cyan" href="https://www.irs.gov/businesses/international-businesses/united-states-income-tax-treaties-a-to-z">visit this page.</a> You can study the text of the treaty to understand how it influences your withholding situation, although I would recommend using the help of a CPA for that as well.
              </p>
            </div>


            <hr class="g-my-20">
            <h4 id="#BusinessOwnedbyUSandNonUSPartners" class="pt-4">U.S. Business Owned by U.S. and Non-U.S. Partners</h4>
            <div class="d-inline-block g-width-55 g-height-2 g-bg-cyan mb-2"></div> 

           <div>
            <p class="pt-3"><strong>Q.</strong>
              Is there any disadvantage of having a U.S. partner in the company owned by non-resident aliens?
              </p>
              <p class="pt-1">
                Not that we know of. The rules of taxation apply first on the entity, and only then on each individual partner, based on each partner’s individual tax situation.
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              Since we have a U.S. partner in our corporation, can we elect it to be S Corporation?
              </p>
              <p class="pt-1">
                No. S Corporations cannot have non-U.S. owners.
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              I am US citizen and I want to open a business with a 50% partner who is a non-resident alien living abroad. What type of taxes will my partner pay if we form a C Corporation?
              </p>
              <p class="pt-1">
                There are pros and cons to both structures for a non-resident. A C-Corp would mean your partner is not necessarily required to file a US tax return. He can be paid dividends from the C-Corp, but as with any C-Corp there is no tax deduction for dividends paid out so the earnings are likely to be double taxed, once by the corporation and then by the owners - in the US for you and in your partner’s country for him - as dividend income.
              </p>
           
             <p class="pt-3"><strong>Q.</strong>
              Double taxation doesn’t sound like a good idea. What if we choose LLC (taxed as partnership) instead?
              </p>
              <p class="pt-1">
                An LLC taxed as partnership would eliminate the double taxation, but definitely subjects the non-US partner to U.S. taxation for his share of earnings and profits from the business. The partner would then have to file a 1040NR and report his share of profits and pay US tax on those profits. The partnership would also need to withhold tax at 30% for the foreign partner. Depending on his earnings the withheld tax would be credited and potentially refunded against what he may owe when he files his individual non-resident tax return.
              </p>
           </div>

            <hr class="g-my-20">
            <h4 id="#USBusinessOwnedbyaForeignBusiness" class="pt-4">U.S. Business Owned by a Foreign Business</h4>
            <div class="d-inline-block g-width-55 g-height-2 g-bg-cyan mb-2"></div> 
           
             <div>
              <p class="pt-3"><strong>Q.</strong>
                Can a non-U.S. company own a U.S. company?
                </p>
                <p class="pt-1">
                  Yes, it can, provided the U.S. company is not S Corporation (or LLC taxed as S Corporation).
                </p>
             
               <p class="pt-3"><strong>Q.</strong>
                Is it better to own the U.S. company with my non-U.S. company from tax point of view?
                </p>
                <p class="pt-1">
                  Not necessarily. Ownership does not control if tax is due on US operations of the business. You will need to consider US taxation of non-resident aliens, and if the profits earned in the US are what is known as income effectively connected to operation of a US business, to understand how taxation would work in your specific case. 
                </p>
             
               <p class="pt-3"><strong>Q.</strong>
                I own a company in my country, and I want to register an LLC to be owned by this company. Can I then distribute U.S. profits of this LLC to my company, and pay the taxes in my country?
                </p>
                <p class="pt-1">
                  It is not uncommon for an online business to avoid US taxation, but there are a number of specific factors that are unique to every business which you will have to consider. As you can see from a previous answer, ownership is not the only factor in defining if tax is due, so cases like this should be discussed with a US based tax professional
                </p>
             
               <p class="pt-3"><strong>Q.</strong>
                What if I own the U.S. company with my non-U.S. company, and the non-U.S. company will sell the U.S. company products for resale for the same price the U.S. company will sell them in the U.S.? I want to avoid having to pay taxes in the U.S.
                </p>
                <p class="pt-1">
                  You would not be able to sell at zero profit, due to what are known as transfer pricing rules, which establish how related entities located in two different taxing jurisdiction must establish the price they charge each other for items that are transferred between themselves.
                </p>
             </div>

             <hr class="g-my-20">
             <h4 id="#USBusinessOwnedbyUSPersonLivingAbroad" class="pt-4">U.S. Business Owned by U.S. Person(s) Living Abroad</h4>
             <div class="d-inline-block g-width-55 g-height-2 g-bg-cyan mb-2"></div> 
           
            <div>
              <p class="pt-3"><strong>Q.</strong>
                I am US citizen and a sole owner of an LLC. I live abroad, and my business is online only, without any physical connection. What kind of tax would I have to pay?
                </p>
                <p class="pt-1">
                  Online businesses are taxed just like any other business for income tax purposes and as a US citizen you are subject to tax on worldwide income. If you are living abroad, you may qualify for an earned income exclusion for wages you earn overseas, but profits from your US business would still be subject to state and federal income taxes in the US.
                </p><p class="pt-1">
                  An individual can qualify for a foreign earned income exclusion, but the amount of exclusion is $97,600 for 2013 and earnings over that amount in any one year are taxable.
                </p>
             
               <p class="pt-3"><strong>Q.</strong>
                I am U.S. citizen living abroad. Is there a way for me to run a business from abroad and avoid being taxed in the U.S.?
                </p>
                <p class="pt-1">
                  There is the potential to avoid or at least delay, US taxation through setting up a non-US subsidiary, but that is typically only temporary as any earnings would be taxable in the US if or when those earnings are brought back into the US.
                </p> <p class="pt-1">
                  Depending on the long term goals of the taxpayer he might at least defer paying US tax, but if he intends to bring that money back into the US at some point it will probably be taxed as foreign profits. There are some methods to further defer or avoid US taxation of repatriated profits, but it is a complex area of the tax code that is very specific to the taxpayer's situation and way beyond what we would attempt to explain here.  
                </p>
            </div>

